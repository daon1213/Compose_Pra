package com.daon.compose_pra.fundamentals

fun main(){
    // Assignment operators (+=, -=, *=, /=, %=)
    var myNum = 5
    myNum += 3
    println("myNum is $myNum")
    myNum *= 4
    println("myNum is $myNum")

    // Increment & Decrement operators (++, --)
    myNum++
    println("myNum is $myNum")
    myNum--
    println("myNum is $myNum")
}